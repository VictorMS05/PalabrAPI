from django.contrib import admin
from django.urls import path, include  # Add include to the imports here
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi

schema_view = get_schema_view(
    openapi.Info(
        title="PalabrAPI",
        default_version='v1',
        description="Esta es una API para el manejo de palabras en español donde puedes agregar, consultar y actualizar palabras del catálogo.\n\nCada palabra del catalogo cuenta con los siguientes atributos: id o clave, palabra, tamaño o longitud, raiz de la palabra y letra inicial.\n\nLa API es de uso público y sin fines de lucro, cuenta con 100 palabras actualmente y tiene documentación en Swagger y Redoc.",
        contact=openapi.Contact(email="victormtzsgo@gmail.com")
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('api/', include('api.urls')),  # Add this line here
    path('swagger<format>/', schema_view.without_ui(cache_timeout=0),
         name='schema-json'),
    path('swagger/', schema_view.with_ui('swagger',
         cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc',
         cache_timeout=0), name='schema-redoc')
]
